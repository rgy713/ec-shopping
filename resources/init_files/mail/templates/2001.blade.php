■■■　クリアゲルクレンズの安全性　■■■----------------------------------------------------

クリアゲルクレンズは、ゲル成分をベースに作り上げています。

この「ゲル成分」というのは、お肌の内側の成分と限りなく近い成分ですので、
ほかのベース基材（オイルetc）と比べて、格段に刺激が少ないのです。

さらに「12の無添加」を実現したことにより、お肌への負担を軽減というより、
むしろ、お肌を保護しながら洗浄できるようになったといっても過言ではありません。
