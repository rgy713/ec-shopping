■■■　クリアゲルクレンズのご使用にあたっての注意事項　■■■---------------------

上記のような症状がある場合には、今一度、
ご使用方法をご確認ください。

●力を入れて擦ったりしていませんか？
→絹豆腐をクレンジングしているイメージで行ってください。
　私どものサロンではクレンジングをチェックして差し上げているのですが、
　思った以上に力が入っている方は多いです。
　赤みが出たりする場合は、こすっている方が大半です。
　とにかくゲルで包み込むように優しく洗ってあげてください。

●お肌の状態をしっかり確認しながら行っていますか？
→もし乾燥がひどいようであれば５分間クレンジングは向きません。
　３分にする・通常の使用方法にするなどしてお肌を観察してみてください。

●クレンジングの量が少なくなっていないですか？
→無駄にたくさん使うのも考えものですが、
　少ない量ですと素肌を擦ってしまい負担がかかります。
　ゲルはオイルのように少量ではクレンジングできませんが、
　その分お肌にきちんと働きかけてくれます。
　素肌をいたわってあげる気持ちで少し多めにご使用ください。

●すすぎはしっかりできていますか？
→これは弊社のクレンジングに限らずですが、
　クレンジング後や洗顔後は、必要以上にすすぐ事がポイントです。
　いくら弊社のように無添加クレンジングだとしても、
　クレンジング後のクレンジング剤には
　メイク汚れや皮脂などの汚れが含まれています。
　ですのでしっかりとすすいであげてください。
　もちろんゴシゴシこすりながらすすぐのは厳禁です。


